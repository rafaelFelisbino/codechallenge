package com.dev.rest.beans;

/**
 *
 * @author Felisbino
 */
public class Stations {
    
    private String id;
    private String latitude;
    private String longitude;
    private String name;
    private String display_name;
    private String zone;
    private String total_lines;
    private String rail;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the latitude
     */
    public String getLatitude() {
        return latitude;
    }

    /**
     * @param latitude the latitude to set
     */
    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    /**
     * @return the longitude
     */
    public String getLongitude() {
        return longitude;
    }

    /**
     * @param longitude the longitude to set
     */
    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the display_name
     */
    public String getDisplay_name() {
        return display_name;
    }

    /**
     * @param display_name the display_name to set
     */
    public void setDisplay_name(String display_name) {
        this.display_name = display_name;
    }

    /**
     * @return the zone
     */
    public String getZone() {
        return zone;
    }

    /**
     * @param zone the zone to set
     */
    public void setZone(String zone) {
        this.zone = zone;
    }

    /**
     * @return the total_lines
     */
    public String getTotal_lines() {
        return total_lines;
    }

    /**
     * @param total_lines the total_lines to set
     */
    public void setTotal_lines(String total_lines) {
        this.total_lines = total_lines;
    }

    /**
     * @return the rail
     */
    public String getRail() {
        return rail;
    }

    /**
     * @param rail the rail to set
     */
    public void setRail(String rail) {
        this.rail = rail;
    }
    
    
    
    
}
